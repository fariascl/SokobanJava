    private void elegirBotonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_elegirBotonActionPerformed
        if (seleccionar.showDialog(null, "Abrir)") == JFileChooser.APPROVE_OPTION){
            File archivo = seleccionar.getSelectedFile();
            if (archivo.getName().endsWith("txt")){
                /* aqui debo poner e instanciar el archivo .soko que sera el nivel del juego*/
                String path = archivo.getAbsolutePath();
                try {
                    Tablero juego = new Tablero(path);
                    juego.setVisible(true);
                } catch (IOException ex) {
                    Logger.getLogger(PantallaInicio.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_elegirBotonActionPerformed

